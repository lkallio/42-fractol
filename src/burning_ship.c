#include "fractol.h"

int	bs_iteration(double *x, double *y)
{
	y[1] = fabs(y[1]) * fabs(x[1] + x[1]) + y[0];
	x[1] = x[2] - y[2] + x[0];
	if (x[1] * x[1] == x[2] && y[1] * y[1] == y[2])
		return (1);
	y[2] = y[1] * y[1];
	x[2] = x[1] * x[1];
	return (0);
}
